package main

import (
	"os"
	"path/filepath"

	_ "github.com/fluffy-bunny/fluffycore-rage-oidc/cmd/server/docs" // docs is generated by Swag CLI, you have to import it.
	internal "github.com/fluffy-bunny/fluffycore-rage-oidc/internal"
	internal_runtime "github.com/fluffy-bunny/fluffycore-rage-oidc/internal/runtime"
	internal_version "github.com/fluffy-bunny/fluffycore-rage-oidc/internal/version"
	fluffycore_cobracore_cmd "github.com/fluffy-bunny/fluffycore/cobracore/cmd"
)

// https://github.com/swaggo/echo-swagger

// @title Swagger Example API
// @version 1.0
// @description This is a sample server Petstore server.
// @termsOfService http://swagger.io/terms/

// @contact.name API Support
// @contact.url http://www.swagger.io/support
// @contact.email support@swagger.io

// @license.name Apache 2.0
// @license.url http://www.apache.org/licenses/LICENSE-2.0.html

// @host localhost:9044
// @BasePath /
// @securityDefinitions.basic BasicAuth

func main() {
	processDirectory, err := filepath.Abs(filepath.Dir(os.Args[0]))
	if err != nil {
		panic(err)
	}
	internal.RootFolder = processDirectory
	startup := internal_runtime.NewStartup()
	fluffycore_cobracore_cmd.SetVersion(internal_version.Version())
	fluffycore_cobracore_cmd.Execute(startup)
}
